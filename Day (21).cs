    var cpy = grid.Select(x => x.ToArray()).ToArray();

    for (int y = 0; y < len; y++)
    {
        for (int x = 0; x < len; x++)
        {
            if (grid[y][x] == true)
            {
                foreach (var d in new[] { (x: 1, y: 0), (x: -1, y: 0), (x: 0, y: -1), (x: 0, y: 1), })
                {
                    if (grid[y + d.y][x + d.x] != null)
                    {
                        cpy[y + d.y][x + d.x] = true;
                    }
                }
                cpy[y][x] = false;
            }
        }
    }

    grid = cpy;

    //if (stepCnt % 100 == 0)
